    private void populateTable() {
         // Create a new thread to run the donor retrieval task
        Thread thread = new Thread(() -> {
            try {
                Registry registry = LocateRegistry.getRegistry("localhost", 1099);
                Blood_Bank_Remote_Interface remoteObject = (Blood_Bank_Remote_Interface) registry.lookup("BloodBank");

                List<Donor> donors = remoteObject.getAllDonors();
                DefaultTableModel model = new DefaultTableModel();
                model.addColumn("Donor Name");
                model.addColumn("Blood Type");
                model.addColumn("Quantity");
                for (Donor donor : donors) {
                    model.addRow(new Object[]{donor.getDonorName(), donor.getBloodType(), donor.getQuantity()});
                }

                // Update the table on the EDT
                SwingUtilities.invokeLater(() -> {
                    Detail_table.setModel(model);
                });
            } catch (RemoteException | NotBoundException ex) {
                Logger.getLogger(Blood_Bank_Client_GUI.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Error getting donors: " + ex.getMessage());
            }
        });

        // Start the thread
        thread.start();
    }
