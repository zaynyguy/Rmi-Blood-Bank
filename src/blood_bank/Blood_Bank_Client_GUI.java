    // Create Donners
    private void Add_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Add_buttonActionPerformed
        // Create a new thread to run the donor creation task
        Thread thread = new Thread(() -> {
            try {
                Registry registry = LocateRegistry.getRegistry("localhost", 1099);
                Blood_Bank_Remote_Interface remoteObject = (Blood_Bank_Remote_Interface) registry.lookup("BloodBank");
                String name = Name_field.getText();
                String bloodType = Blood_group_field.getText();
                double blood = Integer.parseInt(Blood_quantity_field.getText());
                remoteObject.createDonor(name, bloodType, blood);
                JOptionPane.showMessageDialog(null, "Donor created: " + name);
            } catch (RemoteException | NotBoundException ex) {
                Logger.getLogger(Blood_Bank_Client_GUI.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Error creating donor: " + ex.getMessage());
            }
        });

        // Start the thread
        thread.start();
    }//GEN-LAST:event_Add_buttonActionPerformed

